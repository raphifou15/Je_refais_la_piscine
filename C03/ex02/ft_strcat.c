char	*ft_strcat(char *dest, char *src)
{
	int i;
	int j;

	i = 0;
	j = -1;
	while (dest[i])
		i++;
	while (src[++j] && ++i != -1)
		dest[i - 1] = src[j];
	dest[i] = src[j];
	return (dest);
}
