int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int i;

	i = -1;
	while (i < (int)n - 1 && s1[++i] && s2[i] && s1[i] == s2[i])
		;
	if (s1[i] > s2[i])
		return (1);
	return (s1[i] == s2[i]) ? 0 : -1;
}
