#include "ft_hexdump.h"

char	*ft_init_tab_octet(char *tab)
{
	int	i;

	i = -1;
	while (++i < 16)
		tab[i] = (i < 10) ? i + 48 : i + 87;
	tab[i] = '\0';
	return (tab);
}
