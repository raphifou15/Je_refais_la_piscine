#include "ft_do_op.h"

void	ft_putnbr(int nbr)
{
	long	n;

	n = nbr;
	(n < 0) ? ft_putchar('-') : 0;
	n = (n < 0) ? n * -1 : n;
	(n < 10) ? ft_putchar(n + 48) : ft_putnbr(n / 10);
	(n > 9) ? ft_putchar((n % 10) + 48) : n;
}
