void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int tab2[size];
	int x;

	x = -1;
	i = size;
	while (--i >= 0)
		tab2[i] = tab[++x];
	while (++i < size)
		tab[i] = tab2[i];
}
