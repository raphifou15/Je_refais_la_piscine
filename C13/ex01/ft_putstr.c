#include "ft_btree.h"

void	ft_putstr(void *str)
{
	int		i;
	char	*str2;

	str2 = (char *)str;
	i = -1;
	while (str2[++i])
		write(1, &str2[i], 1);
	write(1, "\n", 1);
}
