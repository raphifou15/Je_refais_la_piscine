#include "ft_btree.h"

void	ft_write(void *data)
{
	char	*str;
	int		i;

	str = NULL;
	str = (char *)data;
	i = -1;
	while (str[++i])
		write(1, &str[i], 1);
	write(1, "\n", 1);
}
